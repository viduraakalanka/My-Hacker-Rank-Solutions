import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = 0;
        String str1;

        while (sc.hasNext()){
            i = i+1;
            str1 = sc.nextLine();
            System.out.printf("%d "+str1+ "\n",i);
        }
    }
}

